package me.shy.logging.entity;

public class BSC {
	private String name;
	private String ip;
	private String type;
	private boolean isSelected;
	private String connectionInfro;
	private String currectCmd;
	
	public final String getName() {
		return name;
	}
	public final void setName(String name) {
		this.name = name;
	}
	public final String getIp() {
		return ip;
	}
	public final void setIp(String ip) {
		this.ip = ip;
	}
	public final String getType() {
		return type;
	}
	public final void setType(String type) {
		this.type = type;
	}
	public final boolean isSelected() {
		return isSelected;
	}
	public final void setSelected(boolean isSelected) {
		this.isSelected = isSelected;
	}
	public final String getConnectionInfro() {
		return connectionInfro;
	}
	public final void setConnectionInfro(String connectionInfro) {
		this.connectionInfro = connectionInfro;
	}
	public final String getCurrectCmd() {
		return currectCmd;
	}
	public final void setCurrectCmd(String currectCmd) {
		this.currectCmd = currectCmd;
	}
	
	

}
